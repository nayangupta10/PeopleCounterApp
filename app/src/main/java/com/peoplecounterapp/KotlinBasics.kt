package com.peoplecounterapp

fun main(){
    println("Hello World!")

    //val is immutable variable that could be initalized once not not be change
    val myName="Nayan"
    println("Hello $myName")

    //var is mutable variable that could be change at run time
    // here hisName is address and Sumit is value of this that is stored init.
    var hisName="Gaurav"
    hisName="Sumit"
    print("Hi $hisName")
}